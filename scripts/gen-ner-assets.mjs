#!/usr/bin/env node
// Generate src/ner-assets.js by base64-embedding the ner/ files, so the gateway
// can materialize a working NER directory on disk at runtime even when running as
// a single standalone binary (where ner/ is NOT on the filesystem). Run by the
// build (and committable) — keeps the embedded copy in lockstep with ner/.
import { readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..');
const FILES = ['run.sh', 'server.py', 'requirements.txt'];

const entries = FILES.map((f) => {
  const b64 = readFileSync(join(ROOT, 'ner', f)).toString('base64');
  return `  ${JSON.stringify(f)}: ${JSON.stringify(b64)},`;
}).join('\n');

const out = `// GENERATED by scripts/gen-ner-assets.mjs — do NOT edit. Edit ner/* and re-run.
// Base64 of the bundled spaCy NER server, embedded so a standalone binary can
// write a runnable ner/ directory to disk (the binary has no ner/ on the FS).
export const NER_ASSETS = {
${entries}
};
`;
writeFileSync(join(ROOT, 'src', 'ner-assets.js'), out);
console.log(`gen-ner-assets: wrote src/ner-assets.js (${FILES.join(', ')})`);
